package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		//  1 to 90
		
		for (int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		
		// 60 to 10
		
		for (int a=60; a>=10; a-- ) {
			System.out.print(a+" ");
		}
		System.out.println();
		
		//

	}

}
